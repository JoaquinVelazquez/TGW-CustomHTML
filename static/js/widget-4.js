var electrodomesticosFlecha = document.querySelector(".electrodomesticos-flecha");
var electrodomesticosSub = document.querySelector(".electrodomesticos_sub");

electrodomesticosFlecha.addEventListener('click', function(){
    console.log("Electrodomesticos")
    electrodomesticosFlecha.classList.toggle("rotacion");
    electrodomesticosSub.classList.toggle("visible");
    electrodomesticosSub.classList.toggle("invisible");
})

